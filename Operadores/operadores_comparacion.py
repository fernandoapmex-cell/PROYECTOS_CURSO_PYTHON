print('***operadores de comparacion (relacionales)***')

a,b=56,78
print(f'valor inicial de {a} y {b}')

#operador de igualdad ==
#aca compara si son iguales

resultado = a == b
print(f'resultado de valuar a igual a b es : {resultado}')

#operador de diferente

resultado = a != b

print(f'resultado de valor a es deiferente a b es : {resultado}')

#operador mayor que

resultado=a > b
print(f'resultado de valor a es mayor a b es : {resultado}')

#operador mayor o igual que

resultado=a >= b
print(f'resultado de valor a es mayor o igual a b es : {resultado}')

#operador menor que

resultado=a < b
print(f'resultado de valor a es menor a b es : {resultado}')

#operador menor o igual que

resultado=a <= b
print(f'resultado de valor a es menor o igual a b es : {resultado}')