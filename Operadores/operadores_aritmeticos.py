# Operadores Aritmeticos

a = 10
b= 3

#Suma
print('suma')
suma=a+b
print(f'{suma}')

#resta
print('resta')
resta=a-b
print(f'{resta}')

#Multiplcacion
print('Multiplicacion')
multiplicacion=a*b
print(f'{multiplicacion}')

#Division
print('Division')
division=a/b
print(f'{division:.2f}')

#division entera
print('Division Entera')
division=a//b
print(f'{division}')

#residuo de la division %

modulo=a%b
print(f'residuo de la division: {modulo}')

#exponente(potencia)
exponente= a**b # 10*10*10
print(f'{exponente}')